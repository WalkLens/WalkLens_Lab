namespace UnityEngine.XR.Simulation
{
    enum VoxelGridOrientation
    {
        /// <summary>
        /// The upward direction along positive y-axis.
        /// </summary>
        Up = 0,

        /// <summary>
        /// The downward direction along negative y-axis.
        /// </summary>
        Down = 1,

        /// <summary>
        /// The forward direction along negative z-axis.
        /// </summary>
        Forward = 2,

        /// <summary>
        /// The backward direction along positive z-axis.
        /// </summary>
        Back = 3,

        /// <summary>
        /// The right direction along positive x-axis.
        /// </summary>
        Right = 4,

        /// <summary>
        /// The left direction along negative x-axis.
        /// </summary>
        Left = 5
    }
}
